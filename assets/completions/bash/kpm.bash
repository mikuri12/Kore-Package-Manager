                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        kpm__subcmd__install)
            opts="-a -u -c -h --app-name --use-root --category --help <SOURCES>..."
